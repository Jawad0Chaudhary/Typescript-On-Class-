//Three types of variables: let, var, const
//let
let name = "Jawad";
console.log(typeof name);
console.log(name);
name ="Obaid";
console.log(name);

//var
var name1 = "Osama";
console.log(name1);
var name1 = "Bilal";
console.log(name1);

//const
const age = 21;
console.log(typeof age);
console.log(age);

